export function getDayContent(day) {
  const value = Number(day);
  if (Number.isFinite(value) && value > 28) {
    const overflowIndex = Math.floor(Math.random() * OVERFLOW_AFFIRMATIONS.length);
    return {
      ...DAY_CONTENT["28"],
      title: "Extended Stay",
      affirmation: OVERFLOW_AFFIRMATIONS[overflowIndex],
    };
  }

  const room = value >= 28 ? 28 : value;
  return DAY_CONTENT[String(room)] || DAY_CONTENT["28"];
}
